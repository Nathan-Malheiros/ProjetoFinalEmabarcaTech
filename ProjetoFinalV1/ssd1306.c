// Comandos de inicialização do SSD1306 
const uint8_t init_cmds[] = {
    0xAE,           // Desliga o ecrã temporariamente
    0x20, 0x00,     // Modo de endereçamento de memória horizontal
    0x21, 0x00, 0x7F, // Define coluna de início e fim (0 a 127)
    0x22, 0x00, 0x07, // Define página de início e fim (0 a 7)
    0x40,           // Define linha de início da RAM como 0
    0x81, 0x7F,     // Define contraste (metade)
    
    // --- Comandos de inversão de hardware corrigidos ---
    0xA1,           // Inverte mapeamento de colunas (Segment remap)
    0xC8,           // Inverte scan direction de linhas (COM scan)
    // ---------------------------------------------------

    0xA6,           // Display Normal (não inverso)
    0xA8, 0x3F,     // Define taxa de multiplexação (63)
    0xD3, 0x00,     // Define display offset como 0
    0xD5, 0x80,     // Define freq. de clock do oscilador
    0xD9, 0xF1,     // Define pre-charge period
    0xDA, 0x12,     // Define COM pins hardware configuration
    0xDB, 0x40,     // Define VCOMH deselect level
    0x8D, 0x14,     // Ativa charge pump (necessário para ligar o painel)
    0xAF            // Liga o ecrã
};

