void ssd1306_init(ssd1306_t *disp, i2c_inst_t *i2c) {
    disp->i2c_port = i2c;
    disp->buffer[0] = 0x40; 
    for (int i = 0; i < sizeof(init_cmds); i++) {
        uint8_t cmd[2] = {0x00, init_cmds[i]};
        i2c_write_blocking(disp->i2c_port, SSD1306_I2C_ADDR, cmd, 2, false);
    }
    ssd1306_clear(disp);
    ssd1306_show(disp);
}

void ssd1306_clear(ssd1306_t *disp) {
    memset(&disp->buffer[1], 0, sizeof(disp->buffer) - 1);
}

void ssd1306_show(ssd1306_t *disp) {
    i2c_write_blocking(disp->i2c_port, SSD1306_I2C_ADDR, disp->buffer, sizeof(disp->buffer), false);
}

void ssd1306_draw_pixel(ssd1306_t *disp, uint8_t x, uint8_t y, bool on) {
    if (x >= SSD1306_WIDTH || y >= SSD1306_HEIGHT) return;
    uint16_t index = 1 + x + (y / 8) * SSD1306_WIDTH;
    if (on) disp->buffer[index] |= (1 << (y % 8));
    else disp->buffer[index] &= ~(1 << (y % 8));
}

void ssd1306_draw_string(ssd1306_t *disp, uint8_t x, uint8_t y, const char *str) {
    while (*str) {
        if (*str >= 32 && *str <= 90) { 
            const uint8_t *char_data = font_5x8[*str - 32];
            for (int i = 0; i < 5; i++) {
                for (int j = 0; j < 8; j++) {
                    ssd1306_draw_pixel(disp, x + i, y + j, (char_data[i] >> j) & 0x01);
                }
            }
        }
        x += 6; 
        str++;
    }

}


void ssd1306_draw_icon_scaled(ssd1306_t *disp, uint8_t x, uint8_t y, const uint8_t *bitmap) {
    for (int j = 0; j < 16; j++) {
        for (int i = 0; i < 16; i++) {
            int byte_idx = (j * 2) + (i / 8);
            uint8_t pixel = (bitmap[byte_idx] >> (7 - (i % 8))) & 0x01;
            if (pixel) {
               
                ssd1306_draw_pixel(disp, x + i*2, y + j*2, true);
                ssd1306_draw_pixel(disp, x + i*2 + 1, y + j*2, true);
                ssd1306_draw_pixel(disp, x + i*2, y + j*2 + 1, true);
                ssd1306_draw_pixel(disp, x + i*2 + 1, y + j*2 + 1, true);
            }
        }
    }
}